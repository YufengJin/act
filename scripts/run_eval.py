#!/usr/bin/env python3
"""
act IL evaluation client: WebSocket -> N-episode rollouts -> success rate.

Purpose
-------
Connects to an external policy server over WebSocket, runs N episodes against
the act bimanual sim env, and reports success rate + mean return. The server
is expected to speak the `policy_websocket` protocol — `tests/test_random_policy_server.py`
is the in-repo smoke companion; in real use, point this at any trained
policy server that implements the same protocol and advertises action_dim=14.

Server protocol (see `policy_websocket` package):
 - client connects -> server sends metadata dict (e.g. {"action_dim": 14})
 - per step: client.infer({"qpos": ..., "qvel": ..., "images": ..., "task_description": ...})
            -> {"actions": ndarray of shape (14,)}

Logs and videos land under `<log_dir>/eval/<task>--<YYYYMMDD_HHMMSS>/` so
every run is its own auditable directory; the text log is a stdout mirror
(same lines, same order) and is `flush()`-ed on every write so a `tail -f`
sees it live. Args are dumped to the log head for reproducibility. Success
in act = `max_reward_in_episode >= env.task.max_reward` (4 for both
shipped tasks).

Action layout (14-D, absolute joint positions + normalized gripper):
    [ left_arm_qpos(6), left_gripper(1),
      right_arm_qpos(6), right_gripper(1) ]

Example
-------
    # Terminal A — start a policy server (random here; swap for trained in prod)
    python tests/test_random_policy_server.py --port 8765

    # Terminal B — run eval against it
    python scripts/run_eval.py --policy_server_addr localhost:8765 \\
        --task_name sim_transfer_cube_scripted --n-episodes 10 --max-steps 400

    # Grep the resulting log for failure modes
    grep FAILURE logs/eval/sim_transfer_cube_scripted--*/eval.log

Generated by benchmark-env-generator (IL Category).
"""

import argparse
import atexit
import gc
import os
import signal
import sys
import time
from datetime import datetime
from pathlib import Path
from typing import Optional

import numpy as np

REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from constants import SIM_TASK_CONFIGS
from policy_websocket import WebsocketClientPolicy
from sim_env import BOX_POSE, make_sim_env
from utils import sample_box_pose, sample_insertion_pose, set_seed


ACTION_DIM = 14


def log(msg: str, log_file: Optional["os.PathLike"] = None) -> None:
    """Print to stdout and, if open, also write to a log file (flushed each call)."""
    print(msg)
    if log_file is not None and not log_file.closed:
        log_file.write(msg + "\n")
        log_file.flush()


def set_initial_pose(task_name: str) -> None:
    if "sim_transfer_cube" in task_name:
        BOX_POSE[0] = sample_box_pose()
    elif "sim_insertion" in task_name:
        BOX_POSE[0] = np.concatenate(sample_insertion_pose())


def extract_top_frame(obs) -> Optional[np.ndarray]:
    """Return the 'top' camera RGB frame (H, W, 3) uint8, or None if unavailable."""
    if "images" not in obs:
        return None
    images = obs["images"]
    if "top" in images:
        return np.asarray(images["top"], dtype=np.uint8)
    if images:
        first = next(iter(images.values()))
        return np.asarray(first, dtype=np.uint8)
    return None


def build_observation(ts, task_name: str, phase: str = "step") -> dict:
    obs = ts.observation
    return {
        "qpos": np.asarray(obs["qpos"], dtype=np.float32),
        "qvel": np.asarray(obs.get("qvel", []), dtype=np.float32),
        "images": {n: np.asarray(img) for n, img in obs.get("images", {}).items()},
        "task_description": task_name,
        "__meta__": {
            "v": 1,
            "benchmark": "act",
            "task": str(task_name),
            "task_description": task_name,
            "phase": phase,
        },
    }


def run_episode(env, client, task_name: str, max_steps: int, save_video: bool,
                onscreen_render: bool, log_file) -> tuple:
    """Run a single eval episode. Returns (success, length, total_reward, frames)."""
    set_initial_pose(task_name)
    ts = env.reset()
    client.reset()
    env_max_reward = env.task.max_reward

    if onscreen_render:
        try:
            import matplotlib.pyplot as plt
            ax = plt.subplot()
            plt_img = ax.imshow(env._physics.render(height=480, width=640, camera_id="angle"))
            plt.ion()
        except Exception as e:
            log(f"[warn] onscreen_render setup failed ({e}); falling back", log_file)
            onscreen_render = False

    frames = []
    total_reward = 0.0
    highest_reward = 0.0
    last_step = 0

    for step in range(max_steps):
        last_step = step
        if onscreen_render:
            plt_img.set_data(env._physics.render(height=480, width=640, camera_id="angle"))
            plt.pause(0.001)

        observation = build_observation(ts, task_name)
        if save_video:
            frame = extract_top_frame(ts.observation)
            if frame is not None:
                frames.append(frame)

        t0 = time.time()
        response = client.infer(observation)
        action = np.asarray(response["actions"], dtype=np.float32)
        if action.ndim > 1:
            action = action[0]
        infer_ms = (time.time() - t0) * 1000.0

        if action.shape[0] != ACTION_DIM:
            raise ValueError(
                f"Policy returned action of shape {action.shape}; expected ({ACTION_DIM},)"
            )

        if step % 50 == 0:
            log(f"  step={step}: infer {infer_ms:.1f}ms, action[:4]={action[:4]}", log_file)

        ts = env.step(action)
        r = float(ts.reward) if ts.reward is not None else 0.0
        total_reward += r
        if r > highest_reward:
            highest_reward = r
        if highest_reward >= env_max_reward:
            log(f"  Success at step={step} (reward={highest_reward}).", log_file)
            break

    if onscreen_render:
        try:
            import matplotlib.pyplot as plt
            plt.close()
        except Exception:
            pass

    success = highest_reward >= env_max_reward
    return success, last_step + 1, total_reward, frames


def main():
    parser = argparse.ArgumentParser(
        description="act IL evaluation against a policy server.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--policy_server_addr", required=True,
                        help="host:port of the WebSocket policy server")
    parser.add_argument("--task_name", type=str, default="sim_transfer_cube_scripted",
                        choices=list(SIM_TASK_CONFIGS.keys()),
                        help="Which act task to evaluate")
    parser.add_argument("--n-episodes", dest="n_episodes", type=int, default=10,
                        help="Number of evaluation episodes")
    parser.add_argument("--max-steps", dest="max_steps", type=int, default=None,
                        help="Per-episode step cap (default: task_cfg['episode_len'])")
    parser.add_argument("--seed", type=int, default=1000)
    parser.add_argument("--policy", type=str, default="randomPolicy",
                        help="Policy name for the log header (free-form)")
    parser.add_argument("--log_dir", type=str, default="./logs",
                        help="Parent dir for per-run eval logs (created if missing)")
    parser.add_argument("--save_video", action="store_true",
                        help="Record the 'top' camera stream and write one MP4 per episode.")
    parser.add_argument("--video_fps", type=int, default=30)
    parser.add_argument("--onscreen_render", action="store_true",
                        help="Live matplotlib render (X11 compose only)")
    args = parser.parse_args()

    set_seed(args.seed)
    task_cfg = SIM_TASK_CONFIGS[args.task_name]
    if args.max_steps is None:
        args.max_steps = task_cfg["episode_len"]

    date_str = datetime.now().strftime("%Y%m%d_%H%M%S")
    run_dir = os.path.join(args.log_dir, "eval", f"{args.task_name}--{date_str}")
    os.makedirs(run_dir, exist_ok=True)
    log_path = os.path.join(run_dir, "eval.log")
    log_file = open(log_path, "w")

    def _cleanup(signum=None, frame=None):
        try:
            if not log_file.closed:
                log_file.close()
        finally:
            sys.stdout.flush()
            sys.stderr.flush()
        if signum is not None:
            os._exit(1)

    signal.signal(signal.SIGINT, _cleanup)
    signal.signal(signal.SIGTERM, _cleanup)
    atexit.register(lambda: None if log_file.closed else log_file.close())

    log("=" * 60, log_file)
    log("act IL eval", log_file)
    log("=" * 60, log_file)
    for k, v in sorted(vars(args).items()):
        log(f"  {k}: {v}", log_file)
    log(f"  run_dir: {run_dir}", log_file)
    log("=" * 60, log_file)
    log("", log_file)

    host, port = args.policy_server_addr.rsplit(":", 1)
    client = WebsocketClientPolicy(host=host, port=int(port))
    meta = client.get_server_metadata()
    log(f"server metadata: {meta}", log_file)
    server_dim = meta.get("action_dim") if isinstance(meta, dict) else None
    if server_dim is not None and int(server_dim) != ACTION_DIM:
        raise ValueError(
            f"task_name={args.task_name} expects action_dim={ACTION_DIM}, "
            f"but server returned {server_dim}."
        )

    env = make_sim_env(args.task_name)
    env_max_reward = env.task.max_reward
    log(f"env_max_reward = {env_max_reward}", log_file)

    returns, successes, lengths = [], [], []
    try:
        for ep in range(args.n_episodes):
            log(f"\n--- Episode {ep + 1}/{args.n_episodes} ---", log_file)
            success, length, total_reward, frames = run_episode(
                env=env,
                client=client,
                task_name=args.task_name,
                max_steps=args.max_steps,
                save_video=args.save_video,
                onscreen_render=args.onscreen_render,
                log_file=log_file,
            )
            returns.append(total_reward)
            successes.append(int(success))
            lengths.append(length)

            running_sr = sum(successes) / len(successes) * 100.0
            log(
                f"ep {ep + 1}/{args.n_episodes}: return={total_reward:.2f} success={success} "
                f"length={length} | running_sr={sum(successes)}/{len(successes)} ({running_sr:.1f}%)",
                log_file,
            )

            if args.save_video and frames:
                try:
                    import imageio
                    mp4 = os.path.join(run_dir, f"episode_{ep:03d}_success={success}.mp4")
                    writer = imageio.get_writer(
                        mp4, fps=args.video_fps, format="FFMPEG", codec="libx264")
                    for f in frames:
                        writer.append_data(f)
                    writer.close()
                    log(f"  saved video: {mp4}", log_file)
                except Exception as e:
                    log(f"[warn] imageio write failed: {type(e).__name__}: {e}", log_file)
            del frames
            gc.collect()

        log("", log_file)
        log("=" * 60, log_file)
        log("FINAL RESULTS", log_file)
        log("=" * 60, log_file)
        log(f"  policy:          {args.policy}", log_file)
        log(f"  task:            {args.task_name}", log_file)
        log(f"  episodes:        {args.n_episodes}", log_file)
        log(f"  mean_return:     {np.mean(returns):.3f} +/- {np.std(returns):.3f}", log_file)
        log(f"  success_rate:    {np.mean(successes):.3f}", log_file)
        log(f"  mean_length:     {np.mean(lengths):.1f}", log_file)
        log(f"  log_file:        {log_path}", log_file)
        log("=" * 60, log_file)
    finally:
        try:
            client.close()
        except Exception:
            pass
        if not log_file.closed:
            log_file.close()


if __name__ == "__main__":
    main()
